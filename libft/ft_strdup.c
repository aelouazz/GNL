#include "libft.h"

/*
** -----------------------------------------------------------------------------
** allocates memory to copy @src and returns @new pointing to the copy address |
** -----------------------------------------------------------------------------
*/

char		*ft_strdup(const char *s1)
{
	char	*s;
	int		i;

	if (!(s = (char *)malloc(ft_strlen(s1) * sizeof(char) + 1)))
		return (NULL);
	i = -1;
	while (s1[++i] != '\0')
		s[i] = s1[i];
	s[i] = '\0';
	return (s);
}
